    private void BchooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BchooseActionPerformed
        JFileChooser logoChooser = new JFileChooser(); //creating a new selecting file window
        logoChooser.setFileSelectionMode(JFileChooser.FILES_ONLY); //directories can't be choosen
        logoChooser.setAcceptAllFileFilterUsed(false);        
        // add some new filters
        logoChooser.addChoosableFileFilter(new FileNameExtensionFilter("PNG Images", "png"));
        logoChooser.addChoosableFileFilter(new FileNameExtensionFilter("JPEG Images", "jpeg", "jpg"));
        
        try {
            if (logoChooser.showDialog(this, "Attach") == logoChooser.APPROVE_OPTION){ //if the person pressed ATTACHED
            try {
                logo = logoChooser.getSelectedFile();
                BufferedImage img = ImageIO.read(logoChooser.getSelectedFile()); // read the input image
                ImageIcon imgIcon = new ImageIcon(img); // get an image
                Image image = imgIcon.getImage(); 
                Image newimg = image.getScaledInstance(280, 140,  java.awt.Image.SCALE_SMOOTH); // scale it the smooth way  
                imgIcon = new ImageIcon(newimg);  // transform it back
                Lshow.setIcon(imgIcon); // display the image
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "ERROR during reading the image! Try again...", "ATTENTION",JOptionPane.ERROR_MESSAGE);
                return;
            }
            String filepath = logoChooser.getSelectedFile().getAbsolutePath();
            Lway.setText(filepath); // display the path
        } //if the person pressed CANCEL
        else{
            JOptionPane.showMessageDialog(this, "You did NOT attach the logo!", "ATTENTION",JOptionPane.INFORMATION_MESSAGE);
        }}
        catch (Exception ex) {
                // in case file has image type, but it is not an image
                JOptionPane.showMessageDialog(this, "Can NOT read an image!", "ATTENTION",JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_BchooseActionPerformed

