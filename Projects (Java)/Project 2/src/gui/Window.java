    private void BdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BdeleteActionPerformed
        Contract contract = null;
        try{ DefaultListModel model = (DefaultListModel)Loffers.getModel();
             contract = (Contract)model.getElementAt(Loffers.getSelectedIndex());}
        catch(Exception ex)
                {JOptionPane.showMessageDialog(this, "No current offers are established!", "ERROR",JOptionPane.ERROR_MESSAGE); return;}       
        if(contract == null)
            {JOptionPane.showMessageDialog(this, "Please, select an offer!", "ERROR",JOptionPane.ERROR_MESSAGE); return;}
        
        for (Specialist s : contract.getSpecialists()){s.setAvailable(true);}
        int workersCount = contract.getEmployer().getNumberOfWorkers() - contract.getSpecialists().size();
        contract.getEmployer().setNumberOfWorkers(workersCount);        
        contracts.remove(contract);
        
        CardLayout card = (CardLayout)Pmain.getLayout();
        card.show(Pmain, "options");
        JOptionPane.showMessageDialog(this, "Selected offer was successfully completed!", "THANK YOU",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_BdeleteActionPerformed

