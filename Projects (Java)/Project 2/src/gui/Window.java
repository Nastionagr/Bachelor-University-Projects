    private void Bregistrate5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bregistrate5ActionPerformed
        Employer employer = (Employer)CBemployers.getSelectedItem();
        if(employer == null)
            {JOptionPane.showMessageDialog(this, "Pleease, choose an employer!", "ERROR",JOptionPane.ERROR_MESSAGE); return;}
        
        ArrayList<Specialist> specialists = new ArrayList<Specialist>(); 
        try {DefaultListModel model = (DefaultListModel)Lallwork.getModel();
            for(int i = 0; i< model.getSize(); i++){specialists.add((Specialist)model.getElementAt(i));}}
        catch(Exception ex)
                {JOptionPane.showMessageDialog(this, "Pleease, choose some workers!", "ERROR",JOptionPane.ERROR_MESSAGE); return;}
        if(specialists.size() == 0)
            {JOptionPane.showMessageDialog(this, "Pleease, choose some workers!", "ERROR",JOptionPane.ERROR_MESSAGE); return;}
       
        for(Specialist s : specialists){s.setAvailable(false);}
        employer.setNumberOfWorkers(employer.getNumberOfWorkers() + specialists.size());
        Contract contract = new Contract(employer, specialists);
        contracts.add(contract);
        
        LshowinfoE.setText("");
        Lshowlogo.setText("");
        Lshowlogo.setIcon(null);
        LshowinfoW.setText("");
        Badd.setVisible(false);
        specialists = null;
        
        CardLayout card = (CardLayout)Pmain.getLayout();
        card.show(Pmain, "options");
        JOptionPane.showMessageDialog(this, "The new contract was successfully created!", "THANK YOU",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_Bregistrate5ActionPerformed

