    private void AddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddProductActionPerformed
        Product prod = (Product)ProductName.getSelectedItem();
        
        if (prod == null){            
            JOptionPane.showMessageDialog(this, "Choose the product!", "ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int numberOfProducts = (int)Quantity.getValue();
        ((DefaultListModel)LAllProducts.getModel()).addElement("NAME: " + prod.getName() + "    PCS: " + String.valueOf(numberOfProducts) + "    PRICE: " + String.valueOf(prod.getPrice()));
        Float newTotal = Float.valueOf(TotalSum.getText()) + numberOfProducts * prod.getPrice();
        TotalSum.setText(String.valueOf(newTotal));

        products.add(new Product(prod.getName(), prod.getDetails(), prod.getPrice()));
        quantity.add(numberOfProducts);
        
    }//GEN-LAST:event_AddProductActionPerformed

