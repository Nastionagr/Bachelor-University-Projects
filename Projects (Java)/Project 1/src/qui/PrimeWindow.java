    private void Update1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update1ActionPerformed
        Client updatedClient = (Client)Cchoose.getSelectedItem();
        
        if (updatedClient == null)
        {
            JOptionPane.showMessageDialog(this, "Please select a client!", "ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String name = TCname1.getText();
        String country = TCcountry1.getText();
        String city = TCcity1.getText();
        String address = TCaddress1.getText();
        String postcode = TCpostcode1.getText();
        if(name.isBlank() || country.isBlank() || city.isBlank() || address.isBlank() || postcode.isBlank()){
            JOptionPane.showMessageDialog(this, "All fields have to be filled!", "ERROR",JOptionPane.ERROR_MESSAGE);
        }
        else{
            updatedClient.setName(TCname1.getText());
            updatedClient.setCountry(TCcountry1.getText());
            updatedClient.setCity(TCcity1.getText());
            updatedClient.setAddress(TCaddress1.getText());
            updatedClient.setPostcode(TCpostcode1.getText());

            TCname1.setText("");
            TCcountry1.setText("");
            TCcity1.setText("");
            TCaddress1.setText("");
            TCpostcode1.setText("");
            CardLayout card = (CardLayout)Medium.getLayout();
            card.show(Medium, "options");
            JOptionPane.showMessageDialog(this, "A client was updated!", "THANK YOU",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Update1ActionPerformed

